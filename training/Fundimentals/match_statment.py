# Match is Switch.
# No need to break statement.
# case _ means default.

a = 2
match a:
    case 1:
        print ("Sunday")
    case 2:
        print ("Monday")
    case 3:
        print ("Tusday")
    case 4:
        print ("Wednesday")
    case 5:
        print ("Thursday")
    case 6:
        print ("Friday")
    case 6:
        print ("Saturday")
    case _:
        print ("Invalid Value")